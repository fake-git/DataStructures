package LinkedList;

public class Node {

    public Object data;
    public Node next;


    public Object getData() {
        return data;
    }


    public void setData(Object data) {
        this.data = data;
    }


    public Node getNext() {
        return next;
    }


    public void setNext(Node next) {
        this.next = next;
    }
}
